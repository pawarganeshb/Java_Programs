import java.util.Scanner;

public class Squar {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter a number: ");
        int num = sc.nextInt();
        int Squar = num * num;
        System.out.println("Squar of " + num + " is: " + Squar);
    }

}
